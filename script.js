document.addEventListener('DOMContentLoaded', () => {
    // Lucide Icons are initialized in the HTML via script tag for simplicity in this setup

    // Intersection Observer for scroll animations
    const observerOptions = {
        root: null,
        rootMargin: '0px',
        threshold: 0.1
    };

    const observer = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateY(0)';
                observer.unobserve(entry.target);
            }
        });
    }, observerOptions);

    const fadeElements = document.querySelectorAll('.fade-in-up');
    fadeElements.forEach(el => {
        el.style.opacity = '0';
        el.style.transform = 'translateY(30px)';
        el.style.transition = 'opacity 0.8s ease-out, transform 0.8s ease-out';
        observer.observe(el);
    });

    // Mobile Navigation (Optional addition for future expansion)
    const hamburger = document.querySelector('.hamburger');
    const navLinks = document.querySelector('.nav-links');

    if (hamburger) {
        hamburger.addEventListener('click', () => {
            // Basic toggle for mobile menu (requires CSS support for 'active' class on nav-links if we fully implement a drawer)
            // For now, simpler interaction or just a placeholder for future enhancement
            alert("Mobile menu clicked! (Enhancement: Add mobile menu toggle logic in CSS)");
        });
    }

    // Smooth Scroll for Anchor Links (Native CSS smooth-scroll handles most, but this ensures support)
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            document.querySelector(this.getAttribute('href')).scrollIntoView({
                behavior: 'smooth'
            });
        });
    });
});
